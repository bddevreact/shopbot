def get_text(language, key, **kwargs):
    """Get translated text for a given language and key"""
    if language not in LANGUAGES:
        language = 'EN'  # Default to English
    
    if key not in LANGUAGES[language]:
        # Fallback to English if key not found
        if key in LANGUAGES['EN']:
            return LANGUAGES['EN'][key].format(**kwargs)
        return key  # Return key if not found anywhere
    
    return LANGUAGES[language][key].format(**kwargs)

def get_available_languages():
    """Get list of available languages"""
    return [(code, LANGUAGES[code]['name'], LANGUAGES[code]['flag']) for code in LANGUAGES.keys()]

def is_valid_language(language_code):
    """Check if language code is valid"""
    return language_code in LANGUAGES
